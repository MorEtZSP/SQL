CREATE TABLE if not EXISTS DIRECTIONS (
ID INT AUTO_INCREMENT,
name VARCHAR(200) NOT NULL,
PRIMARY KEY(ID)
);

CREATE TABLE if not EXISTS GROUPS (
ID INT AUTO_INCREMENT,
name VARCHAR(200) NOT NULL,
DIR_ID INT,
PRIMARY KEY(ID),
FOREIGN KEY(DIR_ID) REFERENCES DIRECTIONS (ID)
);

CREATE TABLE if not EXISTS STUDS (
ID INT AUTO_INCREMENT,
name VARCHAR(200) NOT NULL,
BIRTH date not NULL,
ADRESS VARCHAR(200),
E-mail VARCHAR(200),
GROUP_ID INT,
BUDGET BOOL,
PRIMARY KEY(ID),
FOREIGN KEY(GROUP_ID) REFERENCES GROUPS (ID)
);

CREATE TABLE if not EXISTS PHONE (
ID INT AUTO_INCREMENT,
STUD_ID INT,
number VARCHAR(200) NOT NULL,
PRIMARY KEY(ID),
FOREIGN KEY(STUD_ID) REFERENCES STUDS (ID)
);

CREATE TABLE if not EXISTS SUBJECTS (
ID INT AUTO_INCREMENT,
name VARCHAR(200) NOT NULL,
PRIMARY KEY(ID)
);

CREATE TABLE if not EXISTS TEACHERS (
ID INT AUTO_INCREMENT,
name VARCHAR(200) NOT NULL,
PRIMARY KEY(ID)
);

CREATE TABLE if not EXISTS TEACHERS_DIRS_SUBJECTS (
ID INT AUTO_INCREMENT,
SUBJECT_ID INT,
TEACHER_ID INT,
DIRECTION_ID INT,
PRIMARY KEY(ID),
FOREIGN KEY(SUBJECT_ID) REFERENCES SUBJECTS (ID),
FOREIGN KEY(DIRECTION_ID) REFERENCES DIRECTIONS (ID),
FOREIGN KEY(TEACHER_ID) REFERENCES TEACHERS (ID)
);

CREATE TABLE if not EXISTS PAIRTIME (
ID INT AUTO_INCREMENT,
ST_PAIR TIME,
END_PAIR TIME,
PRIMARY KEY(ID)
);

CREATE TABLE `MARKS` (
`ID` int(11) NOT NULL AUTO_INCREMENT,
`STUD_ID` int(11) DEFAULT NULL,
`SUBJ_TEACHER_ID` int(11) DEFAULT NULL,
`MARK` int(11) DEFAULT NULL,
PRIMARY KEY (`ID`),
KEY `STUD_ID` (`STUD_ID`),
KEY `SUBJ_TEACHER_ID` (`SUBJ_TEACHER_ID`),
CONSTRAINT `MARKS_ibfk_1` FOREIGN KEY (`STUD_ID`) REFERENCES `STUDS` (`ID`),
CONSTRAINT `MARKS_ibfk_2` FOREIGN KEY (`SUBJ_TEACHER_ID`) REFERENCES `TEACHERS_DIRS_SUBJECTS` (`ID`)
);

CREATE TABLE `LESSONS_SCH` (
`ID` int(11) NOT NULL AUTO_INCREMENT,
`SUBJ_TEACHER_ID` int(11) DEFAULT NULL,
`NUMBER_PAIR` int(11) DEFAULT NULL,
`date` date DEFAULT NULL,
PRIMARY KEY (`ID`),
KEY `SUBJECT_ID` (`SUBJ_TEACHER_ID`),
KEY `NUMBER_PAIR` (`NUMBER_PAIR`),
CONSTRAINT `LESSONS_SCH_ibfk_1` FOREIGN KEY (`SUBJ_TEACHER_ID`) REFERENCES `TEACHERS_DIRS_SUBJECTS` (`ID`),
CONSTRAINT `LESSONS_SCH_ibfk_2` FOREIGN KEY (`NUMBER_PAIR`) REFERENCES `PAIRTIME` (`ID`)
);

CREATE TABLE `ATTEND` (
`ID` int(11) NOT NULL AUTO_INCREMENT,
`SCHEDULE_ID` int(11) DEFAULT NULL,
`STUD_ID` int(11) DEFAULT NULL,
`PRESENSE` tinyint(1) DEFAULT NULL,
PRIMARY KEY (`ID`),
KEY `SCHEDULE_ID` (`SCHEDULE_ID`),
KEY `STUD_ID` (`STUD_ID`),
CONSTRAINT `ATTEND_ibfk_1` FOREIGN KEY (`SCHEDULE_ID`) REFERENCES `LESSONS_SCH` (`ID`),
CONSTRAINT `ATTEND_ibfk_2` FOREIGN KEY (`STUD_ID`) REFERENCES `STUDS` (`ID`)
);

